package com.example.elderlymeeting;

public class User {

    public String fullName, age, email, bio, picture;
    public String[] hobby;
    public User(){

    }

    public User (String fullName, String age, String email){
        this.fullName = fullName;
        this.age = age;
        this.email = email;
        this.bio = bio;
        this.hobby = hobby;
        this.picture = picture;
    }
}
